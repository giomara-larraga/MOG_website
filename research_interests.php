                <h2>Current Research Directions</h2>
                A summary of the current research interests is the following:
                <ul class="default-list">
                  <li>Interactive multiobjective optimization methods</li>
                  <li>
                    Solving problems with computationally expensive function
                    evaluations
                  </li>

                  <li>Software development</li>
                  <li>Real-world applications</li>
                  <li>
                    Data-driven decision support, prescriptive and decision
                    analytics
                  </li>
                  <li>Explainable artificial intelligence</li>
                  <li>Robustness</li>
                </ul>

                Methods developed include
                <ul class="default-list">
                  <li>NIMBUS - classification-based interactive method</li>
                  <li>
                    Pareto Navigator - interactive method in particular for
                    computationally expensive problems
                  </li>

                  <li>
                    NAUTILUS - family of interactive methods where the decision
                    maker can gain without sacrifice
                  </li>

                  <li>
                    PAINT - method preparing a comultationally inexpensive
                    surrogate problem
                  </li>

                  <li>
                    K-RVEA - evolutionary method for computationally expensive
                    problems
                  </li>
                </ul>

                <p>
                  The most well-known interactive method developed in the group
                  is NIMBUS. NIMBUS is a classification-based method where the
                  decision maker classifies objective functions to indicate the
                  kind of changes that are desired in the current Pareto optimal
                  solution to make it better. Several variants of the method
                  have been published during the years and the synchronous
                  version is currently in use.
                  <a href="http://www.mit.jyu.fi/optgroup/thesis_topics.php">
                    <b>
                      Examples of thesis topics relating to the current research
                      direction for bachelor, master and doctoral students are
                      available here.
                    </b></a
                  >
                </p>

                <p>
                  Among more recently developed interactive methods we can
                  mention Pareto Navigator which has been directed for
                  computationally expensive problems. The idea is to create an
                  approximation of the Pareto optimal set and enable the
                  decision maker to navigate on it. On the approximation,
                  changes of trade-offs can be seen in real-time and then any
                  interesting solution can be projected to the real Pareto
                  optimal set. Without the approximation, the navigation would
                  be too slow because calculating new Pareto optimal solutions
                  would take too much time. On the other hand, the interactive
                  NAUTILUS method questions the idea of considering only Pareto
                  optimal solutions throughout the solution process. Instead,
                  the method starts from the nadir point and allows finding the
                  most preferred solution without anchoring and the need of
                  giving up in some objectives. This can be useful also for
                  group decision making situations. We have developed several
                  variants of NAUTILUS where the decision maker has different
                  ways to specify preference information in order to direct the
                  solution process
                </p>

                <p>
                  Because many methods developed in the group are motivated by
                  practical applications, it is important that this work is also
                  brought close to people that in real-life face the actual
                  problems and apply the methods. Therefore, the group has
                  developed and is developing several different interactive
                  tools for multiobjective optimization.
                </p>

                <p>
                  One can say that the most widely available interactive
                  multiobjective optimization software is WWW-NIMBUS (available
                  at
                  <a href="http://nimbus.it.jyu.fi/">http://nimbus.it.jyu.fi/</a
                  >), an implementation of the NIMBUS method. WWW-NIMBUS is a
                  web-based software freely available for academic teaching and
                  research use around the world (the first version was published
                  as early as in 1995). Based on the WWW-NIMBUS software, the
                  group has also developed a commercial optimization tool
                  IND-NIMBUS, which is a desktop application operating on Linux
                  and Windows platforms. IND-NIMBUS has been lately used for
                  most industrial applications considered. It is available for
                  industrial partners and a demo-version is available for
                  interested parties (<a href="http://ind-nimbus.it.jyu.fi/"
                    >http://ind-nimbus.it.jyu.fi/</a
                  >).
                </p>

                <p>
                  After testing with demo versions, the implementation of the
                  new interactive methods Pareto Navigator and NAUTILUS has been
                  started so that they can become more widely applicable. In
                  method and related software development, the group is paying
                  special attention to intuitive human-computer interaction.
                  Because interactive methods are supposed to support learning,
                  the way preference information is acquired from and new
                  insight into the problem is presented to the decision maker
                  plays a very important role in the success of the solution
                  process. This research contains user interface and interaction
                  design, usability research, information visualization and
                  visual analytic environments.
                </p>

                <p>
                  The group has been active in building bridges between the MCDM
                  and evolutionary multiobjective optimization (EMO)
                  communities. Examples of hybrid method development include
                  approaches for estimating the nadir point utilizing EMO and
                  achievement scalarizing functions. In addition, the efficiency
                  and accuracy of EMO methods have been improved by hybridizing
                  scalarizing functions and local search in them. Preference
                  information has been also incorporated in EMO methods in the
                  form of a reference point improving efficiency and enabling
                  concentration on interesting Pareto optimal solutions.
                </p>

                <p>
                  Alongside the general theoretical and methodological
                  development, recently, the development of so-called
                  approximation methods has been considered in the group. These
                  methods aim, in a way or another at building or utilizing an
                  approximation of the objective functions or the Pareto optimal
                  set (e.g. meta models, polyhedral and tangent plane
                  approximations). An approximation of the Pareto optimal set is
                  especially useful in the case of industrial applications
                  because problem related models are typically computationally
                  very time-consuming to operate.
                </p>

                <p>
                  Another research direction is related how to tackle with
                  uncertainty in multiobjective optimization problems, that is,
                  how to compare solution alternatives under uncertainty and in
                  changing environments. This research contains studies related
                  to uncertainties in process parameters, optimization of the
                  production plant concepts under different production tasks and
                  production plant design through bilevel multiobjective problem
                  formulation.
                </p>
              </div>

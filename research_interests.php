              <h2>Current Research Directions</h2>
              A summary of our current research interests is the following:
              <ul class="default-list">
                <li>Interactive multiobjective optimization methods</li>

                <li>Solving problems with computationally expensive function evaluations </li>

                <li>Software development </li>

                <li>Visualization aimed towards multiobjective optimization and decision making </li>

                <li>Real-world applications </li>

                <li>Data-driven decision support, prescriptive and decision analytics </li>

                <li>Explainable artificial intelligence and explanations in multiobjective optimization </li>

                <li>Robustness and dealing with deep uncertainty </li>

                <li>Comparing interactive methods with experiments involving humans and with artificial decision makers (ADMs) and developing appropriate quality indicators </li>

                <li>Group decision making </li>
              </ul>

              <a href="http://www.mit.jyu.fi/optgroup/thesis_topics.php">
                <b>
                  Examples of thesis topics relating to the current research
                  direction for bachelor, master and doctoral students are
                  available here.
                </b></a> Examples of the multiobjective optimization methods we have developed:
              <ul class="default-list">
                <li>NIMBUS - classification-based interactive method </li>

                <li>Pareto Navigator - interactive method, in particular, for computationally expensive problems </li>

                <li>Nonconvex Pareto Navigator – extension of Pareto Navigator for nonconvex problems </li>

                <li>NAUTILUS - family of interactive methods where the decision maker can gain without sacrifice, that is, trade-off-free methods NAUTILUS, NAUTILUS-2, E-NAUTILUS, NAUTILUS Navigator and O-NAUTILUS </li>

                <li>PAINT - method preparing a computationally inexpensive surrogate problem </li>

                <li>SURROGATE-ASF – for computationally expensive problems </li>

                <li>ANOVA-MOP – for computationally expensive problems with a large number of variables</li>

                <li>DESMILS – interactive method for multi-item lot sizing problems </li>

                <li>PBEA – interactive evolutionary method with reference point as preference information </li>

                <li>interactive RVEA – interactive evolutionary method with four different preference information types </li>

                <li>K-RVEA - evolutionary method for computationally expensive problems </li>

                <li>Interactive K-RVEA – interactive version of K-RVEA </li>

                <li>LiMoRo - lightly robust interactive method for problems with uncertainty </li>

                <li>IOPIS - new paradigm in interactive evolutionary optimization </li>
              </ul>

              <p>
                The most well-known interactive method developed in the group is NIMBUS. NIMBUS is a classification-based method where the decision maker classifies objective functions to indicate the kind of changes that are desired in the current Pareto optimal solution to make it better. Several variants of the method have been published during the years and the synchronous version is currently in use.
              </p>

              <p>
                Among more recently developed interactive methods we can mention Nonconvex Pareto Navigator for computationally expensive problems. The idea is to create an approximation of the Pareto optimal set and enable the decision maker to navigate on it. On the approximation, changes of trade-offs can be seen in real-time and then any interesting solution can be projected to the real Pareto optimal set. Without the approximation, the navigation would be too slow because calculating new Pareto optimal solutions would take too much time.
              </p>

              <p>
                On the other hand, the interactive methods in the NAUTILUS family question the idea of considering only Pareto optimal solutions throughout the solution process. Instead, the methods start from the nadir point (with very poor values for all objective functions) and allow finding the most preferred solution without anchoring and the need of giving up in some objective functions. This can be useful also for group decision making situations. In different members of the NAUTILUS method family, the decision maker has different ways to specify preference information to direct the solution process. In NAUTILUS Navigator, we combine elements of navigation methods with the NAUTILUS philosophy.
              </p>
              <p>
                Because many methods developed in the group are motivated by practical applications, it is important that this work is also brought close to people who in real-life face the actual problems and apply the methods. Therefore, the group has developed and is developing several different interactive tools for multiobjective optimization.
              </p>

              <p>
                The first (even internationally) widely available interactive multiobjective optimization software was WWW-NIMBUS (available at
                <a href="http://nimbus.it.jyu.fi/">http://nimbus.it.jyu.fi/</a>), an implementation of the NIMBUS method. WWW-NIMBUS is a web-based software freely available for academic teaching and research use around the world (the first version was published as early as in 1995). Based on the WWW-NIMBUS software, the group also developed a commercial optimization tool IND-NIMBUS, which is a desktop application operating on Linux and Windows platforms. IND-NIMBUS was used for many industrial applications A demo-version is available for interested parties (<a href="http://ind-nimbus.it.jyu.fi/">http://ind-nimbus.it.jyu.fi/</a>).
              </p>

              <p>
                For broader visibility and applicability, the group has switched to open source development and develops the open source software framework DESDEO (desdeo.it.jyu.fi). The methods developed are implemented in DESDEO. In method and related software development, the group is paying special attention to intuitive human-computer interaction. Because interactive methods are supposed to support learning, the way preference information is acquired from and new insight into the problem is presented to the decision maker plays a very important role in the success of the solution process. This research contains user interface and interaction design, usability research, information visualization and visual analytic environments.
              </p>

              <p>
                The group has been active in building bridges between the multiple criteria decision making (MCDM) and evolutionary multiobjective optimization (EMO) communities. Examples of hybrid method development include approaches for estimating the nadir point utilizing EMO and achievement scalarizing functions. In addition, the efficiency and accuracy of EMO methods have been improved by hybridizing scalarizing functions and local search in them. Preference information has been also incorporated in EMO methods, for example, in the form of a reference point improving efficiency and enabling concentration on interesting Pareto optimal solutions.
              </p>

              <p>
                Alongside the general theoretical and methodological development the development of so-called approximation methods has been considered. These methods aim, in a way or another at building or utilizing an approximation of the objective functions or the Pareto optimal set (e.g. metamodels, Kriging, polyhedral or tangent plane approximations). An approximation of the Pareto optimal set is especially useful if models are computationally very time-consuming to operate. Another research focus is data-driven problems where the starting point is data and a problem formulation must be prepared based on the data. This leads to decision analytics and data-enabled decision making.
              </p>

              <p>
                Another research direction is related how to tackle with uncertainty in multiobjective optimization problems, that is, how to compare solution alternatives under uncertainty and in changing environments. This research contains studies related to robustness and deep uncertainty.
              </p>

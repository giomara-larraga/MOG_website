                <h2>Research Profile</h2>
                <p>
                  Real-life industrial problems typically need to be considered
                  from very different perspectives. This leads to the need of
                  optimizing several conflicting objectives simultaneously. In
                  the light of this, it is quite natural that one of the main
                  driving forces behind the research of the group is
                  multiobjective optimization.
                </p>
                <p>
                  In multiobjective settings with continuous variables, there
                  typically are infinitely many Pareto optimal solutions and the
                  ultimate task of the decision maker is to determine the best,
                  that is, the most preferred Pareto optimal solution which is
                  to be implemented and tested in practice. However, it is very
                  important that before the actual decision about the final
                  solution takes place the decision maker should gain a good
                  understanding about the trade-offs between the solution
                  alternatives. The final decision should be firmly grounded.
                </p>
                <p>
                  Benefits of multiobjective optimization include that the
                  conflicting objectives are taken into account simultaneously
                  leading to an overall insight of the problem. Therefore,
                  multiobjective optimization can bring about a significant
                  competitive advantage when compared to widely used simplistic
                  approaches where e.g. only some primary objective is optimized
                  and other, although important, objectives are left without a
                  special attention. In different fields of industry, there is a
                  lot of need for multiobjective treatment but not yet enough
                  awareness about it and, thus, the group also faces the
                  challenge of disseminating information about the potential of
                  multiobjective optimization.
                </p>
                <p>
                  One of the main research interests in the group is interactive
                  multiobjective optimization. It supports the decision maker
                  actively in finding the 'best' Pareto optimal solution by
                  continuously involving him/her and his/her preferences in the
                  solution process to guide the search. The continuous
                  involvement enables the decision maker to learn about one's
                  preferences and the problem/phenomenon considered as well as
                  interdependencies between the objectives.
                </p>
                <p>
                  In addition to MCDM and especially interactive multiobjective
                  optimization, evolutionary multiobjective optimization, and
                  different hybrid methods (incorporating benefits of different
                  types of approaches), our group shares also interest in
                  general mathematical programming, global optimization (e.g.
                  evolutionary algorithms and memetic approaches) and
                  optimization software development including, in particular,
                  usability issues. Actually, the group is one of the few groups
                  actively working with implementations of interactive
                  multiobjective optimization methods.
                </p>
              </div>

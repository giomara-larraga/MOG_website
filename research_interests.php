              <h2>Research Profile</h2>
              <p>Real-life decision making problems typically need to be considered from very different perspectives. In other words, the goodness of a decision to be made must be characterized with multiple perspectives. This leads to the need to optimize several conflicting objective functions simultaneously. Therefore, the focus of the research group is multiobjective optimization.
              </p>
              <p>In multiobjective settings (with continuous variables), there typically are infinitely many Pareto optimal solutions and the ultimate task of the decision maker is to determine the best, that is, the most preferred Pareto optimal solution which is to be implemented and tested in practice. However, it is very important that before the actual decision about the final solution takes place, the decision maker should gain a good understanding about the trade-offs. The final decision should be firmly grounded.
              </p>
              <p>Benefits of multiobjective optimization include that the conflicting objective functions are taken into account simultaneously leading to an overall insight of the problem. Therefore, multiobjective optimization can bring about a significant competitive advantage when compared to widely used simplistic approaches where e.g. only some primary objective is optimized and other, although important, objectives are left without a special attention or converted into constraints. In different fields of life, there is a lot of need for multiobjective treatment but not yet enough awareness about it and, thus, the group also faces the challenge of disseminating information about the potential of multiobjective optimization.
              </p>
              <p>One of the main research interests in the group is interactive multiobjective optimization. It supports the decision maker actively in finding the most preferred Pareto optimal solution by continuously involving him/her and his/her preferences in the solution process to guide the search. The continuous involvement enables the decision maker to learn about the feasibility of one's preferences and the problem/phenomenon considered as well as interdependencies between the objectives.
              </p>
              <p>In addition to interactive multiobjective optimization methods, evolutionary multiobjective optimization methods, and different hybrid methods (incorporating benefits of different types of approaches), our group shares also interest in dealing with uncertainty, solving computationally expensive or data-driven problems, supporting groups of decision makers, developing visualizations and optimization software development including, in particular, usability issues. Actually, the group is the only group actively working with implementations of interactive multiobjective optimization methods.
              </p>
              <p>We have vast international and national collaboration networks as can be seen e.g. in our list of publications. We conduct research visits and host visitors to boost collaboration. We have collaborators e.g. in Spain, UK, Sweden, Poland, Austria, Norway, USA, France, Czech Republic and the Netherlands. </p>
